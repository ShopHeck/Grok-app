#!/usr/bin/env node

/**
 * AgentDesk Chrome Extension Build Script
 *
 * Builds the extension for different environments by:
 * 1. Copying all extension files to dist/extension/
 * 2. Replacing API_BASE and config values per environment
 * 3. Generating the manifest with correct version
 * 4. Creating a .zip ready for Chrome Web Store upload
 *
 * Usage:
 *   node scripts/build-extension.js [environment]
 *
 * Environments:
 *   development  — http://localhost:3000
 *   staging      — https://staging.agentdesk.app
 *   production   — https://agentdesk.app (default)
 *
 * Output:
 *   dist/extension/          — unpacked extension (for local testing)
 *   dist/agentdesk-ext-v{VERSION}-{ENV}.zip — packaged for store upload
 */

const fs = require("fs");
const path = require("path");
const { execSync } = require("child_process");

// ============ CONFIGURATION ============

const ENV_CONFIGS = {
  development: {
    API_BASE: "http://localhost:3000",
    APP_URL: "http://localhost:3000",
    ENV: "development",
  },
  staging: {
    API_BASE: "https://staging.agentdesk.app",
    APP_URL: "https://staging.agentdesk.app",
    ENV: "staging",
  },
  production: {
    API_BASE: "https://agentdesk.app",
    APP_URL: "https://agentdesk.app",
    ENV: "production",
  },
};

const EXTENSION_SRC = path.resolve(__dirname, "../extension");
const DIST_DIR = path.resolve(__dirname, "../dist/extension");

// ============ MAIN ============

function main() {
  const env = process.argv[2] || "production";

  if (!ENV_CONFIGS[env]) {
    console.error(`Unknown environment: ${env}`);
    console.error(`Available: ${Object.keys(ENV_CONFIGS).join(", ")}`);
    process.exit(1);
  }

  const config = ENV_CONFIGS[env];
  const packageJson = JSON.parse(
    fs.readFileSync(path.resolve(__dirname, "../package.json"), "utf-8")
  );
  const version = packageJson.version || "1.0.0";

  console.log(`\n⚡ Building AgentDesk Extension`);
  console.log(`   Environment: ${env}`);
  console.log(`   API Base:    ${config.API_BASE}`);
  console.log(`   Version:     ${version}\n`);

  // Step 1: Clean and create dist directory
  if (fs.existsSync(DIST_DIR)) {
    fs.rmSync(DIST_DIR, { recursive: true });
  }
  fs.mkdirSync(DIST_DIR, { recursive: true });

  // Step 2: Copy all extension files
  copyDirSync(EXTENSION_SRC, DIST_DIR);
  console.log("   ✓ Copied extension files");

  // Step 3: Replace config.js with env-specific values
  const configContent = `/**
 * Extension Configuration — ${env}
 * Generated by scripts/build-extension.js at ${new Date().toISOString()}
 */

// eslint-disable-next-line no-unused-vars
const AGENTDESK_CONFIG = {
  API_BASE: "${config.API_BASE}",
  APP_URL: "${config.APP_URL}",
  EXTENSION_ID: "",
  VERSION: "${version}",
  ENV: "${config.ENV}",
};
`;
  fs.writeFileSync(path.join(DIST_DIR, "config.js"), configContent);
  console.log("   ✓ Generated config.js");

  // Step 4: Replace hardcoded API_BASE in JS files
  const jsFiles = [
    "background/service-worker.js",
    "popup/popup.js",
  ];

  for (const file of jsFiles) {
    const filePath = path.join(DIST_DIR, file);
    if (fs.existsSync(filePath)) {
      let content = fs.readFileSync(filePath, "utf-8");
      // Replace the hardcoded API_BASE constant
      content = content.replace(
        /const API_BASE = ["'][^"']+["'];?/g,
        `const API_BASE = AGENTDESK_CONFIG?.API_BASE || "${config.API_BASE}";`
      );
      fs.writeFileSync(filePath, content);
    }
  }
  console.log("   ✓ Replaced API_BASE in JS files");

  // Step 5: Update manifest version
  const manifestPath = path.join(DIST_DIR, "manifest.json");
  const manifest = JSON.parse(fs.readFileSync(manifestPath, "utf-8"));
  manifest.version = version;

  // Add config.js to background scripts via importScripts workaround
  // (MV3 service workers can't use import, but we reference the global)

  fs.writeFileSync(manifestPath, JSON.stringify(manifest, null, 2));
  console.log("   ✓ Updated manifest version");

  // Step 6: Validate required files exist
  const requiredFiles = [
    "manifest.json",
    "background/service-worker.js",
    "popup/popup.html",
    "popup/popup.js",
    "content/gmail.js",
    "content/linkedin.js",
    "content/google-docs.js",
    "content/auth-bridge.js",
    "styles/content.css",
    "styles/popup.css",
    "config.js",
  ];

  const missing = requiredFiles.filter(
    (f) => !fs.existsSync(path.join(DIST_DIR, f))
  );

  if (missing.length > 0) {
    console.error(`\n   ✗ Missing files: ${missing.join(", ")}`);
    process.exit(1);
  }

  // Check for icon files (warn if missing, don't fail)
  const iconFiles = [
    "assets/icon-16.png",
    "assets/icon-32.png",
    "assets/icon-48.png",
    "assets/icon-128.png",
  ];
  const missingIcons = iconFiles.filter(
    (f) => !fs.existsSync(path.join(DIST_DIR, f))
  );
  if (missingIcons.length > 0) {
    console.warn(`   ⚠ Missing icons (needed for store): ${missingIcons.join(", ")}`);
  }

  console.log("   ✓ Validated required files");

  // Step 7: Create ZIP for Chrome Web Store
  const zipName = `agentdesk-ext-v${version}-${env}.zip`;
  const zipPath = path.resolve(__dirname, "../dist", zipName);

  try {
    execSync(`cd "${DIST_DIR}" && zip -r "${zipPath}" . -x "*.DS_Store"`, {
      stdio: "pipe",
    });
    console.log(`   ✓ Created ${zipName}`);
  } catch {
    console.warn("   ⚠ zip command not available — skipping .zip creation");
    console.warn("     Install zip or use Chrome's 'Pack extension' feature");
  }

  console.log(`\n✅ Build complete!`);
  console.log(`   Unpacked: dist/extension/`);
  console.log(`   Load in Chrome: chrome://extensions → Load unpacked → dist/extension/`);
  if (fs.existsSync(zipPath)) {
    console.log(`   Store upload: dist/${zipName}\n`);
  }
}

// ============ HELPERS ============

function copyDirSync(src, dest) {
  fs.mkdirSync(dest, { recursive: true });
  const entries = fs.readdirSync(src, { withFileTypes: true });

  for (const entry of entries) {
    const srcPath = path.join(src, entry.name);
    const destPath = path.join(dest, entry.name);

    if (entry.isDirectory()) {
      copyDirSync(srcPath, destPath);
    } else {
      fs.copyFileSync(srcPath, destPath);
    }
  }
}

// ============ RUN ============

main();
